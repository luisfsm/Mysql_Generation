create table TB_PRODUTOS(
ID 				bigint PRIMARY KEY auto_increment,
NOME	    	VARCHAR(100),
preco			decimal(10,2),
ATIVO			BOOLEAN	DEFAULT 1,
ID_CATEGORIA	BIGINT,
CONSTRAINT FK_PRODUTOS_CATEGORIA FOREIGN KEY (ID_CATEGORIA) references TB_CATEGORIA(ID) 
);

