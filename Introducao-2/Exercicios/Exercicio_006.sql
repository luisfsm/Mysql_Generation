CREATE DATABASE db_cursoDaMinhaVida;
USE db_cursoDaMinhaVida;

CREATE TABLE TB_CATEGORIA(
ID 			bigint PRIMARY KEY auto_increment,
CATEGORIA	VARCHAR(50),
ATIVO		BOOLEAN DEFAULT 1
);

